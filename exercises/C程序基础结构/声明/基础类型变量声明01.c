//           还可以通过初始化器, 将它的初始状态设置成你想要的样子
//
// 例如: 我想要一个整数, 那么我可以写: int a;
//       这里 a 就是标识符(我给它的名字)
//       int 就限定了 a 的类型是整型
//       但这时 a 还没有被初始化, 数值是 "随机的" (你可以暂时理解为随机的)
// 
//       int a = 10;
//       这是 a 等于 10

