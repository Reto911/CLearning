// 说明符与限定符   声明符与初始化器(可选);
// 
// 其中 声明符提供附加类型信息及/或要声明的标识符

// 说回人话: 先从简单的例子开始, 就是你可以通过 类型说明符 表示你想要的C的数据(或对象)的类型
